// Use Push to add element to an Array as the last element array
var numbers = [90, 80, 70, 60];
numbers.push(50);
console.log(numbers);


// Use Pop to get element in Array
numbers.pop();
console.log(numbers)

//Use Unshift to add element to an array as the first lelement.
numbers.unshift(10);
console.log(numbers);

//Use Shift to get first element to an array
numbers.shift();
console.log(numbers);